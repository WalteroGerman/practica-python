### Operadores Aritméticos ###
print(3 + 4)
print(3 * 4)
print(3 / 4)
print(10 % 3) # Te da el resto de la division.
print(31 // 3) # Divide dando un numero entero aproximado
print(4 ** 2) # Calcular exponente
print(4 * 2 + 10 / 2 // 2)

print("Hola " + "Qué tal? ") # Concatena las palabras con el signo +
print("Hola " + str(5))
print("Hola " * 3) # Hola Hola Hola

### Operadores Comparativos ###
print(3 > 4)
print(3 < 4)
print(3 >= 4)
print(3 <= 4)
print(3 == 4)
print(3 != 4) #Distinto

print("Hola" > "Python")
print("Hola" < "Python")
print("Hola" >= "Python") #Ordenación alfabética por ASCII
print(len("Hola") <= len("Python")) #Cuenta caracteres
print("Hola" == "Hola")
print("Hola" != "Python")

### Operadores Lógicos ###
print(3 > 4 and "Hola" > "Python") #Conjunción Verdadero cuando los 2 son V
print(3 > 4 or "Hola" < "Python") #Disyunción Verdadero cuando 1 es V
print(not(3 > 4))                 #Negación devuelve lo contrario
